	/**
	* Returns the poll_id => poll array
	*
	* @param	object	databaseobject	The database object to run the query against
	* @param	string	mixed			Table database type
	* @param	string	mixed			The prefix to the table name i.e. 'vb3_'
	* @param	int		mixed			Start point
	* @param	int		mixed			End point
	*
	* @return	array
	*/
	function get_phpMyForum_poll_details(&$Db_object, &$databasetype, &$tableprefix, $start_at, $per_page)
	{
		$return_array = array();

		// Check that there is not a empty value
		if(empty($per_page)) { return $return_array; }

		if ($databasetype == 'mysql')
		{
			// The question, date etc
			$details_list = $Db_object->query("SELECT * FROM " . $tableprefix . "poll ORDER BY id LIMIT " . $start_at . "," . $per_page);

			while ($detail = $Db_object->fetch_array($details_list))
			{
				$choices = array();
				$votes = array();
				$poll_option = array();
				
				$return_array["$detail[id]"] = $detail;
				
				// Choices
				$options =  $Db_object->query("SELECT * FROM " . $tableprefix . "poll_option WHERE poll_id = " . $detail['id'] . " ORDER BY id");
			
				while ($option = $Db_object->fetch_array($options))
				{
					$choices[]		= $option['text'];
					$votes[]		= $option['votes'];
					$poll_option[]	= $option['id'];
				}
				
				// Voters
				$voters_choice = $Db_object->query("SELECT * FROM " . $tableprefix . "poll_vote WHERE option_id IN (" . implode(',', $poll_option) . ")");

				while ($choice = $Db_object->fetch_array($voters_choice))
				{
					$poll_voters["$choice[user_id]"] = $choice['option_id'];
				}				
				
				$return_array["$detail[id]"]['numberoptions'] 	= count($choices);
				$return_array["$detail[id]"]['voters'] 		= array_sum($votes);
				$return_array["$detail[id]"]['options'] 		= implode('|||', $choices);
				$return_array["$detail[id]"]['votes'] 			= implode('|||', $votes);
				$return_array["$detail[id]"]['poll_voters'] 	= $poll_voters;
			}				
		}
		else
		{
			return false;
		}
		return $return_array;
	}


